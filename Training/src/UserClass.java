public class UserClass {

    String name;
    int age;

    UserClass() {

        age = 18;

    }

    UserClass(int pAge) {
        age = pAge;
    }

    void setName(String pName) {

        name = pName;

    }

    void setAge(int pAge) {

        age = pAge;

    }

    String getName() {

        return name;
    }

    int getAge() {

        return age;

    }


}
